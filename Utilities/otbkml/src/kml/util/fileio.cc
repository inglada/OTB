#include "kml/util/fileio.h"
#include <fstream>

bool ReadFileToString(const char* filename, std::string* result) {
  std::ifstream input_file(filename);
  if (!input_file.good()) {
    return false;
  }
  result->clear();
  const unsigned int buffer_size = 1024;
  char buffer[buffer_size];

  while(!input_file.eof() && input_file.good()) {
    input_file.read(buffer, buffer_size);
    result->append(buffer, input_file.gcount());
  }
  return true;
}

